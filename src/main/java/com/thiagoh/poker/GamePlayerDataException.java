package com.thiagoh.poker;

public class GamePlayerDataException extends PortalException{

	public GamePlayerDataException() {
		super();
	}

	public GamePlayerDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public GamePlayerDataException(String message) {
		super(message);
	}

	public GamePlayerDataException(Throwable cause) {
		super(cause);
	}

}
